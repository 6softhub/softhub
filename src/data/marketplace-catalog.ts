export type CatalogBadge =
  | "NEW" | "TRENDING" | "BEST SELLER" | "AI READY"
  | "OFFLINE" | "SAAS" | "TOP RATED" | "DEAL" | "PRO";

export type CatalogProduct = {
  slug: string;
  name: string;
  description: string;
  price: string;          // display price, e.g. "$249"
  period?: string;        // e.g. "/mo" or "lifetime"
  badges: CatalogBadge[];
  version: string;
  updated: string;        // e.g. "Jul 2026"
};

export type CatalogCategory = {
  slug: string;
  title: string;
  tagline: string;
  /** Two OKLCH colors used for the thumbnail gradient. */
  palette: [string, string];
  /** Lucide icon name — resolved via marketplace-icons. */
  icon: string;
  /** Visual motif shown inside every thumbnail of this category. */
  motif: MotifKey;
  products: CatalogProduct[];
};

