/* ---------- helpers to build a product row quickly ---------- */

const P = (
  name: string,
  description: string,
  price: string,
  period: string,
  badges: CatalogBadge[] = [],
): CatalogProduct => ({
  slug: name.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, ""),
  name,
  description,
  price,
  period,
  badges,
  version: pickVersion(name),
  updated: pickUpdated(name),
});

function hash(s: string) {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) { h ^= s.charCodeAt(i); h = Math.imul(h, 16777619); }
  return h >>> 0;
}
function pickVersion(name: string) {
  const h = hash(name);
  return `v${(h % 8) + 1}.${(h >> 3) % 10}.${(h >> 6) % 10}`;
}
function pickUpdated(name: string) {
  const months = ["Jan","Feb","Mar","Apr","May","Jun","Jul","Aug","Sep","Oct","Nov","Dec"];
  const h = hash(name);
  return `${months[h % 12]} 2026`;
}

